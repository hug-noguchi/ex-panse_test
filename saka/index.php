		$(".accordion-title").on("click", function () {
			let findElm = $(this).next(".accordion-box");
			$(findElm).slideToggle();

			if ($(this).hasClass("open")) {
				$(this).removeClass("open");
			} else {
				$(this).addClass("open");
			}
		});
	});
